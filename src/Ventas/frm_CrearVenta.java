    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:        
        String ventas = txt_Ventas.getText().trim();
        String gastos = txt_Gastos.getText().trim();

        if (ventas.isEmpty() || gastos.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese todos los datos", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!validarDatos(ventas, gastos)) {
            return;
        }

        DefaultTableModel modelTable = (DefaultTableModel) tbl_TablaInforme.getModel();
        int numFilas = modelTable.getRowCount();

        // Buscar un mes no registrado
        String MesSeleccionado = null;
        for (String mes : meses) {
            boolean encontrado = false;
            for (int i = 0; i < numFilas; i++) {
                if (mes.equals(modelTable.getValueAt(i, 0))) {
                    encontrado = true;
                    break;
                }
            }
            if (!encontrado) {
                MesSeleccionado = mes;
                break;
            }
        }

        if (MesSeleccionado == null) {
            JOptionPane.showMessageDialog(null, "Todos los meses ya están registrados.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        modelTable.addRow(new Object[]{MesSeleccionado, ventas, gastos});
        JOptionPane.showMessageDialog(null, "Datos añadidos", "OK", JOptionPane.INFORMATION_MESSAGE);

        txt_Ventas.setText("");
        txt_Gastos.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

