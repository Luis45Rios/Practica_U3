    public boolean validarDatos(String ventas, String gastos) {
        try {
            float valorVentas = Float.parseFloat(ventas);
            float valorGastos = Float.parseFloat(gastos);
            if (valorVentas < 0 || valorGastos < 0) {
                JOptionPane.showMessageDialog(null, "Ingrese valores positivos", "ERROR", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor numérico", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

